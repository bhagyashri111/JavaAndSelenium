package CoreJava;

public class operators {

	public static void main(String[] args) {
		
		int a = 40;
		int b = 10;
		int c;
		
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a/b);
		System.out.println(a%b);
		
		
		System.out.println(a>b);
		System.out.println(a!=b);
		System.out.println(a==b);
		
		
		boolean p = true;
		boolean q = false;
		System.out.println(p & q);
		System.out.println(p || q);
	}

}
